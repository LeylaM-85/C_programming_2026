//Kəsr Ədədlər və Simvollar (float, char)

#include <stdio.h>

int main() {
    float bal = 85.5;      // Kəsr ədəd
    char sinif_herfi = 'A'; // Tək simvol (tək dırnaq daxilində)

    printf("İmtahan balı: %.1f\n", bal); // %.1f nöqtədən sonra 1 rəqəm göstərir
    printf("Sinfim: %c\n", sinif_herfi);  // %c simvollar üçündür

    return 0;
}